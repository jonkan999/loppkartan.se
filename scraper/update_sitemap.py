def update_sitemap(sitemap_path, sub_domain, clean_filename):
    sitemap_url = f"https://loppkartan.se/{sub_domain}/{clean_filename}"
    sitemap_url = sitemap_url.replace('&', '&amp;')

    with open(sitemap_path, 'r', encoding='utf-8') as f:
        sitemap = f.read()
        if sitemap_url in sitemap:
            print(f"Skipped adding '{sitemap_url}' to sitemap as it already exists.")
            return
        sitemap = sitemap.replace("</urlset>",
                                  f'<url>\n  <loc>{sitemap_url}</loc>\n  <lastmod>{datetime.now().isoformat()}+00:00</lastmod>\n  <priority>0.8</priority>\n</url></urlset>')

    with open(sitemap_path, 'w', encoding='utf-8') as f:
        f.write(sitemap)

def clean_filename(name):
    name = name.replace('-', ' ').replace('/', '').replace('å', 'a').replace('ä', 'a').replace('ö', 'o').replace(
        'Å', 'A').replace('Ä', 'A').replace('Ö', 'O').lower()
    return '-'.join(name.split()).lower()

